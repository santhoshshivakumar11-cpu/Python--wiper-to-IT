def greet_engineer(name):
    """This function greets any engineer by name"""
    print(f"Welcome to Engine Room, {name}! 🚢")
    print("Safety first. Chief is watching.")

# Call the function 3 times with different names
greet_engineer("Santhosh")
greet_engineer("2/E")
greet_engineer("ETO")